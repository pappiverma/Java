package com.Arrays;

public class OneDimeArray {
    public static void main(String[] args) {
        int [][]   a= new  int[][]{{6,7,8}, {9,11,15,67}} ;

        for(int i=0; i<a.length;i++){
            for(int j=0; j< a.length;j++){
        System.out.print(a[i][j]+ " ");

            }
            System.out.println();
        }
        
    }
    
}
